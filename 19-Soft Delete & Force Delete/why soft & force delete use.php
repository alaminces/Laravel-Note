২. Force Delete:
Force Delete ডেটাবেজ থেকে রেকর্ড স্থায়ীভাবে মুছে ফেলার জন্য ব্যবহার করা হয়। একবার Force Delete করা হলে সেই রেকর্ড আর রিকভার করা সম্ভব নয়।

Force Delete ব্যবহারের কারণ:

ডেটার স্থায়ী মুছা: যখন ডেটা স্থায়ীভাবে মুছে ফেলার প্রয়োজন হয়, যেমন সিকিউরিটি বা আইনি কারণে।
ডেটাবেজ ক্লিন রাখা: কোনো ডেটা অপ্রয়োজনীয় হলে সেটি পুরোপুরি মুছে ফেলে ডেটাবেজের সাইজ কমানো।
উদাহরণ:

$post = Post::find(1);
$post->forceDelete(); // This will permanently delete the post

# Soft Delete বনাম Force Delete এর মধ্যে পার্থক্য:

>> Soft Delete :
রেকর্ডটি ডেটাবেজে থাকে কিন্তু অদৃশ্য।	
রেকর্ডটি পরে পুনরুদ্ধার করা যায়।	
deleted_at কলামে টাইমস্ট্যাম্প যোগ হয়।	


>> Force Delete :
রেকর্ডটি ডেটাবেজ থেকে সম্পূর্ণভাবে মুছে যায়।
রেকর্ডটি পুনরুদ্ধার করা সম্ভব নয়।
deleted_at এর প্রয়োজন নেই।


কেন ব্যবহার করা হয়:
Soft Delete ব্যবহার করা হয় যেখানে ডেটার ট্র্যাকিং বা রিকভারির প্রয়োজন রয়েছে।
Force Delete ব্যবহার করা হয় যেখানে ডেটা স্থায়ীভাবে মুছে ফেলার প্রয়োজন হয় এবং আর কোনো প্রয়োজন নেই।

সংক্ষেপে: Soft Delete ডেটা রিকভারির সুবিধা দেয়, যেখানে Force Delete স্থায়ীভাবে ডেটা মুছে দেয়।