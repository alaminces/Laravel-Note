
লারাভেলে Soft Delete এবং Force Delete এর ব্যবহার ঃ
১. Soft Delete:
Soft Delete একটি টেকনিক যার মাধ্যমে ডেটাবেজ থেকে রেকর্ড সম্পূর্ণরূপে মুছে ফেলা হয় না, বরং সেটি একটি বিশেষ কলামে (সাধারণত deleted_at) ডিলেটের সময় স্ট্যাম্প করা হয়। এই কলামটি নাল থাকলে রেকর্ডটি একটিভ থাকে এবং একটি টাইমস্ট্যাম্প থাকলে এটি ডিলেট হয়েছে ধরে নেয়া হয়।

Soft Delete ব্যবহারের কারণ:

ডেটা রিকভারি: মুছে ফেলা ডেটা সহজে পুনরুদ্ধার করা যায়। ভুল করে কোনো গুরুত্বপূর্ণ রেকর্ড ডিলেট হলে সেটিকে ফেরত আনার সুযোগ থাকে।
অডিটিং/ট্র্যাকিং: সিস্টেমে কোন ডেটা কখন ডিলেট করা হয়েছে তা ট্র্যাক রাখা যায়। এটি অডিটিং এবং রিপোর্টিং-এর ক্ষেত্রে সহায়ক।
ডেটা স্টোরেজ: রেকর্ডটি ডিলেট হয়েছে কিনা জানতে পারা যায় এবং এটির অন্য ডেটা স্টোরেজ থেকে মুছে না ফেললেও হবে।
উদাহরণ:

$post = Post::find(1);
$post->delete(); // This will perform a soft delete

$deletedPosts = Post::onlyTrashed()->get(); // Fetch all soft deleted posts

$post->restore(); // This will restore the soft deleted post

